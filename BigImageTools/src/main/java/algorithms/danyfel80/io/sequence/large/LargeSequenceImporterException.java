package algorithms.danyfel80.io.sequence.large;

@SuppressWarnings("serial")
public class LargeSequenceImporterException extends RuntimeException {
	public LargeSequenceImporterException() {
		super();
	}

	public LargeSequenceImporterException(String message) {
		super(message);
	}

	public LargeSequenceImporterException(Throwable cause) {
		super(cause);
	}

	public LargeSequenceImporterException(String message, Throwable cause) {
		super(message, cause);
	}
}
