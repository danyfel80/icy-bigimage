package algorithms.danyfel80.io.sequence.large;

@SuppressWarnings("serial")
public class LargeSequenceExporterException extends RuntimeException {
	public LargeSequenceExporterException() {
		super();
	}

	public LargeSequenceExporterException(String message) {
		super(message);
	}

	public LargeSequenceExporterException(Throwable cause) {
		super(cause);
	}

	public LargeSequenceExporterException(String message, Throwable cause) {
		super(message, cause);
	}
}
